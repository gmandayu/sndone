                        doc.ExportCaption(TipePenyaluran);
                        doc.ExportCaption(KategoriPenyaluran);
                        doc.ExportCaption(NamaDokumen);
                    } else {
                        doc.ExportCaption(IdTemplate);
                        doc.ExportCaption(NamaTemplate);
                        doc.ExportCaption(IdModa);
                        doc.ExportCaption(JenisProses);
                        doc.ExportCaption(Keterangan);
                        doc.ExportCaption(DibuatOleh);
                        doc.ExportCaption(TanggalDibuat);
                        doc.ExportCaption(DiperbaruiOleh);
                        doc.ExportCaption(TanggalDiperbarui);
                        doc.ExportCaption(TipePenimbunan);
                        doc.ExportCaption(TipePenyaluran);
                        doc.ExportCaption(KategoriPenyaluran);
                        doc.ExportCaption(NamaDokumen);
                    }
                    doc.EndExportRow();
                }
            }
            int recCnt = startRec - 1;
            // Read first record for View Page // DN
            if (exportType == "view") {
                await dataReader.ReadAsync();
            // Move to and read first record for list page // DN
            } else {
                if (Connection.SelectOffset) {
                    await dataReader.ReadAsync();
                } else {
                    for (int i = 0; i < startRec; i++) // Move to the start record and use do-while loop
                        await dataReader.ReadAsync();
                }
            }
            int rowcnt = 0; // DN
            do { // DN
                recCnt++;
                if (recCnt >= startRec) {
                    rowcnt = recCnt - startRec + 1;

                    // Page break
                    if (ExportPageBreakCount > 0) {
                        if (rowcnt > 1 && (rowcnt - 1) % ExportPageBreakCount == 0)
                            doc.ExportPageBreak();
                    }
                    LoadListRowValues(dataReader);

                    // Render row
                    RowType = RowType.View; // Render view
                    ResetAttributes();
                    await RenderListRow();
                    if (!doc.ExportCustom) {
                        doc.BeginExportRow(rowcnt); // Allow CSS styles if enabled
                        if (exportType == "view") {
                            await doc.ExportField(IdTemplate);
                            await doc.ExportField(NamaTemplate);
                            await doc.ExportField(IdModa);
                            await doc.ExportField(JenisProses);
                            await doc.ExportField(Keterangan);
                            await doc.ExportField(DibuatOleh);
                            await doc.ExportField(TanggalDibuat);
                            await doc.ExportField(DiperbaruiOleh);
                            await doc.ExportField(TanggalDiperbarui);
                            await doc.ExportField(TipePenimbunan);
                            await doc.ExportField(TipePenyaluran);
                            await doc.ExportField(KategoriPenyaluran);
                            await doc.ExportField(NamaDokumen);
                        } else {
                            await doc.ExportField(IdTemplate);
                            await doc.ExportField(NamaTemplate);
                            await doc.ExportField(IdModa);
                            await doc.ExportField(JenisProses);
                            await doc.ExportField(Keterangan);
                            await doc.ExportField(DibuatOleh);
                            await doc.ExportField(TanggalDibuat);
                            await doc.ExportField(DiperbaruiOleh);
                            await doc.ExportField(TanggalDiperbarui);
                            await doc.ExportField(TipePenimbunan);
                            await doc.ExportField(TipePenyaluran);
                            await doc.ExportField(KategoriPenyaluran);
                            await doc.ExportField(NamaDokumen);
                        }
                        doc.EndExportRow(rowcnt);
                    }
                }

                // Call Row Export server event
                if (doc.ExportCustom)
                    RowExport(doc, dataReader);
            } while (recCnt < stopRec && await dataReader.ReadAsync()); // DN
            if (!doc.ExportCustom)
                doc.ExportTableFooter();
        }

